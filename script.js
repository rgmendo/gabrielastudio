function renderService(serviceKey) {
  const selectedService = localizedServices[currentLanguage][serviceKey];

  if (!selectedService) {
    return;
  }

  currentServiceKey = serviceKey;
  elements.serviceTitle.textContent = selectedService.name;
  elements.serviceDescription.textContent = selectedService.description;
  elements.serviceBestFor.textContent = selectedService.bestFor;
  elements.serviceResult.textContent = selectedService.result;
  elements.serviceVibe.textContent = selectedService.vibe;
  elements.featuredServiceName.textContent = selectedService.name;
  elements.featuredServiceSummary.textContent = selectedService.heroSummary;
  elements.featuredServiceTime.textContent = selectedService.heroTime;
  elements.featuredServiceFocus.textContent = selectedService.heroFocus;

  serviceTabs.forEach((tab) => {
    const isSelected = tab.dataset.service === serviceKey;
    tab.classList.toggle("is-active", isSelected);
    tab.setAttribute("aria-pressed", String(isSelected));
  });
}

function applyLanguage(language) {
  currentLanguage = language;
  updateStaticCopy(language);
  renderService(currentServiceKey);

  try {
    window.localStorage.setItem("gabriela-studio-language", language);
  } catch (error) {
    // Ignore storage access issues for local viewing.
  }
}

serviceTabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    renderService(tab.dataset.service);
  });
});

elements.languageSwitch?.addEventListener("click", () => {
  applyLanguage(currentLanguage === "en" ? "es" : "en");
});

if (menuToggle && siteNav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = siteNav.classList.toggle("is-open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      siteNav.classList.remove("is-open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });
}

faqQuestions.forEach((question) => {
  question.addEventListener("click", () => {
    const item = question.closest(".faq-item");
    const isOpen = question.getAttribute("aria-expanded") === "true";

    faqQuestions.forEach((otherQuestion) => {
      otherQuestion.setAttribute("aria-expanded", "false");
      otherQuestion.closest(".faq-item")?.classList.remove("is-open");
    });

    question.setAttribute("aria-expanded", String(!isOpen));
    item?.classList.toggle("is-open", !isOpen);
  });
});

if ("IntersectionObserver" in window) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");

          const matchingLink = navLinks.find(
            (link) => link.getAttribute("href") === `#${entry.target.id}`
          );

          navLinks.forEach((link) => link.classList.remove("is-current"));
          matchingLink?.classList.add("is-current");
        }
      });
    },
    {
      threshold: 0.35
    }
  );

  sections.forEach((section) => observer.observe(section));
} else {
  sections.forEach((section) => section.classList.add("is-visible"));
}

